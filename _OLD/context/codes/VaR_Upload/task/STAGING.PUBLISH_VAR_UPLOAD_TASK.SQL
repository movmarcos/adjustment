create or replace task DVLP_RAPTOR_NEWADJ.STAGING.PUBLISH_VAR_UPLOAD_TASK
	warehouse=PROD_RAPTOR_WH_XS
	schedule='1 MINUTE'
	when SYSTEM$STREAM_HAS_DATA('STAGING.IDM_VAR_UPLOAD_STREAM')
	as MERGE INTO ADJUSTMENT.IDM_VAR_UPLOAD AS tgt
USING STAGING.IDM_VAR_UPLOAD_STREAM_DATA_MERGE AS src
   ON equal_null(src.COBID,tgt.COBID)
  AND equal_null(src.ENTITY_CODE,tgt.ENTITY_CODE)
  AND equal_null(src.SOURCE_SYSTEM_CODE,tgt.SOURCE_SYSTEM_CODE)
  AND equal_null(src.BOOK_CODE,tgt.BOOK_CODE)
  AND equal_null(src.CURRENCY_CODE,tgt.CURRENCY_CODE)
  AND equal_null(src.SCENARIO_DATE_ID,tgt.SCENARIO_DATE_ID)
  AND equal_null(src.TRADE_CODE,tgt.TRADE_CODE)
  AND equal_null(src.VAR_SUB_COMPONENT_ID,tgt.VAR_SUB_COMPONENT_ID)
  AND equal_null(src.FILE_NAME,tgt.FILE_NAME)
  AND IFNULL(tgt.IS_DELETED,FALSE) = FALSE
  AND equal_null(src.DEPARTMENT_CODE,tgt.DEPARTMENT_CODE)
WHEN MATCHED AND (src.DML_TYPE = 'U' OR src.DML_TYPE = 'I') THEN UPDATE
SET
	tgt.ADJUSTMENT_VALUE_IN_USD = src.ADJUSTMENT_VALUE_IN_USD,
    tgt.RUN_STATUS = src.RUN_STATUS,
    tgt.CATEGORY = src.CATEGORY,
    tgt.DETAIL = src.DETAIL,
	tgt.IS_DELETED = FALSE, 
	tgt.DELETED_BY = NULL, 
	tgt.DELETED_DATE = NULL
WHEN MATCHED AND src.DML_TYPE = 'D' THEN UPDATE 
SET  
	tgt.RUN_STATUS = src.RUN_STATUS,
	tgt.IS_DELETED = TRUE, 
	tgt.DELETED_BY = 'SYSTEM', 
	tgt.DELETED_DATE = current_timestamp()::TIMESTAMP_NTZ
WHEN NOT MATCHED AND (src.DML_TYPE = 'I' OR src.DML_TYPE = 'U') THEN INSERT 
(
	COBID,
	ENTITY_CODE,
	SOURCE_SYSTEM_CODE,
	BOOK_CODE,
	CURRENCY_CODE,
	SCENARIO_DATE_ID,
	TRADE_CODE,
	VAR_SUB_COMPONENT_ID,
	ADJUSTMENT_VALUE_IN_USD,
	PROCESS_TYPE,
	ADJUSTMENT_TYPE,
	USERNAME,
	RUN_STATUS,
	FILE_NAME,
	GLOBAL_REFERENCE,
	CATEGORY,
	DETAIL,
	IS_DELETED,
	DEPARTMENT_CODE
	)
VALUES (
	src.COBID,
	src.ENTITY_CODE,
	src.SOURCE_SYSTEM_CODE,
	src.BOOK_CODE,
	src.CURRENCY_CODE,
	src.SCENARIO_DATE_ID,
	src.TRADE_CODE,
	src.VAR_SUB_COMPONENT_ID,
	src.ADJUSTMENT_VALUE_IN_USD,
	src.PROCESS_TYPE,
	src.ADJUSTMENT_TYPE,
	'',
	src.RUN_STATUS,
	src.FILE_NAME,
	src.GLOBAL_REFERENCE,
	src.CATEGORY,
	src.DETAIL,
	FALSE,
	src.DEPARTMENT_CODE
	);