create or replace task DVLP_RAPTOR_NEWADJ.STAGING.PUBLISH_SCALING_ADJUSTMENT_TASK
	warehouse=PROD_RAPTOR_WH_M
	schedule='1 MINUTE'
	when SYSTEM$STREAM_HAS_DATA('STAGING.IDM_SCALING_ADJUSTMENT_STREAM')
	as MERGE
INTO
	DIMENSION.ADJUSTMENT AS tgt
		USING STAGING.IDM_SCALING_ADJUSTMENT_STREAM_DATA AS src
ON tgt.GLOBAL_REFERENCE = src.GLOBAL_ID
	AND tgt.COBID = src.COBID
WHEN MATCHED THEN
UPDATE
SET
	tgt.PROCESS_TYPE = src.PROCESS_TYPE,
	tgt.ADJUSTMENT_TYPE = src.ADJUSTMENT_TYPE,
	tgt.SOURCE_COBID = src.SOURCE_COBID,
	tgt.ENTITY_CODE = src.ENTITY_CODE,
	tgt.SOURCE_SYSTEM_CODE = src.SOURCE_SYSTEM_CODE,
	tgt.DEPARTMENT_CODE = src.DEPARTMENT_CODE,
	tgt.BOOK_CODE = src.BOOK_CODE,
	tgt.CURRENCY_CODE = src.CURRENCY_CODE,
	tgt.ADJUSTMENT_VALUE_IN_USD = src.ADJUSTMENT_VALUE_IN_USD,
	tgt.CREATED_DATE = src.CREATED_DATE,
	tgt.USERNAME = src.USERNAME,
	tgt.RUN_STATUS = src.RUN_STATUS,
	tgt.REASON = src.REASON,
	tgt.TRADE_TYPOLOGY = src.TRADE_TYPOLOGY,
	tgt.TRADE_CODE = src.TRADE_CODE,
	tgt.SCALE_FACTOR = src.SCALE_FACTOR,
	tgt.TRADER_CODE = src.TRADER_CODE,
	tgt.VAR_COMPONENT_ID = src.VAR_COMPONENT_ID,
	tgt.VAR_SUB_COMPONENT_ID = src.VAR_SUB_COMPONENT_ID,
	tgt.SCENARIO_DATE_ID = src.SCENARIO_DATE_ID,
	tgt.APPROVAL_ID = src.APPROVAL_ID,
	tgt.GUARANTEED_ENTITY = src.GUARANTEED_ENTITY,
	tgt.STRATEGY = src.STRATEGY,
	tgt.REGION_KEY = src.REGION_KEY,
	tgt.INSTRUMENT_CODE = src.INSTRUMENT_CODE,
	tgt.SIMULATION_NAME = src.SIMULATION_NAME,
	tgt.SIMULATION_SOURCE = src.SIMULATION_SOURCE,
	tgt.PRODUCT_CATEGORY_ATTRIBUTES = src.PRODUCT_CATEGORY_ATTRIBUTES,
	tgt.IS_DELETED = src.IS_DELETED,
	tgt.DELETED_BY = IFF(src.IS_DELETED::BOOLEAN = TRUE,
	'SYSTEM',
	NULL),
	tgt.DELETED_DATE = IFF(src.IS_DELETED::BOOLEAN = TRUE,
	current_timestamp()::TIMESTAMP_NTZ,
	NULL),
	tgt.DAY_TYPE = src.DAY_TYPE,
	tgt.CURVE_CODE = src.CURVE_CODE,
	tgt.MEASURE_TYPE_CODE = src.MEASURE_TYPE_CODE,
	tgt.TENOR_CODE = src.TENOR_CODE,
	tgt.UNDERLYING_TENOR_CODE = src.UNDERLYING_TENOR_CODE,
	tgt.ADJUSTMENT_OCCURRENCE = src.ADJUSTMENT_OCCURRENCE
	WHEN NOT MATCHED
THEN
INSERT
	(
COBID,
	PROCESS_TYPE,
	ADJUSTMENT_TYPE,
	SOURCE_COBID,
	ENTITY_CODE,
	SOURCE_SYSTEM_CODE,
	DEPARTMENT_CODE,
	BOOK_CODE,
	CURRENCY_CODE,
	ADJUSTMENT_VALUE_IN_USD,
	CREATED_DATE,
	USERNAME,
	RUN_STATUS,
	REASON,
	TRADE_TYPOLOGY,
	TRADE_CODE,
	SCALE_FACTOR,
	TRADER_CODE,
	VAR_COMPONENT_ID,
	VAR_SUB_COMPONENT_ID,
	SCENARIO_DATE_ID,
	APPROVAL_ID,
	GUARANTEED_ENTITY,
	STRATEGY,
	REGION_KEY,
	GLOBAL_REFERENCE,
	INSTRUMENT_CODE,
	SIMULATION_NAME,
	SIMULATION_SOURCE,
	PRODUCT_CATEGORY_ATTRIBUTES,
	IS_DELETED,
	DELETED_BY,
	DELETED_DATE,
	DAY_TYPE,
	CURVE_CODE,
	MEASURE_TYPE_CODE,
	TENOR_CODE,
	UNDERLYING_TENOR_CODE,
	ADJUSTMENT_OCCURRENCE
)
VALUES

(
src.COBID,
src.PROCESS_TYPE,
src.ADJUSTMENT_TYPE,
src.SOURCE_COBID,
src.ENTITY_CODE,
src.SOURCE_SYSTEM_CODE,
src.DEPARTMENT_CODE,
src.BOOK_CODE,
src.CURRENCY_CODE,
src.ADJUSTMENT_VALUE_IN_USD,
src.CREATED_DATE,
src.USERNAME,
src.RUN_STATUS,
src.REASON,
src.TRADE_TYPOLOGY,
src.TRADE_CODE,
src.SCALE_FACTOR,
src.TRADER_CODE,
src.VAR_COMPONENT_ID,
src.VAR_SUB_COMPONENT_ID,
src.SCENARIO_DATE_ID,
src.APPROVAL_ID,
src.GUARANTEED_ENTITY,
src.STRATEGY,
src.REGION_KEY,
src.GLOBAL_ID,
src.INSTRUMENT_CODE,
src.SIMULATION_NAME,
src.SIMULATION_SOURCE,
src.PRODUCT_CATEGORY_ATTRIBUTES,
src.IS_DELETED,
IFF(src.IS_DELETED::BOOLEAN = TRUE,
'SYSTEM',
NULL),
IFF(src.IS_DELETED::BOOLEAN = TRUE,
current_timestamp()::TIMESTAMP_NTZ,
NULL),
src.DAY_TYPE,
src.CURVE_CODE,
src.MEASURE_TYPE_CODE,
src.TENOR_CODE,
src.UNDERLYING_TENOR_CODE,
src.ADJUSTMENT_OCCURRENCE
);