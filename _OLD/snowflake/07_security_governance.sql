-- ═════════════════════════════════════════════════════════════════════════════
-- 3. OBJECT TAGGING (Data Governance & Lineage)
-- ═════════════════════════════════════════════════════════════════════════════

CREATE TAG IF NOT EXISTS CORE.DATA_CLASSIFICATION
    ALLOWED_VALUES 'PII', 'CONFIDENTIAL', 'INTERNAL', 'PUBLIC'
    COMMENT = 'Data classification tag for governance';

CREATE TAG IF NOT EXISTS CORE.DATA_DOMAIN
    ALLOWED_VALUES 'ADJUSTMENT', 'FACT', 'AUDIT', 'CONFIG'
    COMMENT = 'Business domain tag';

CREATE TAG IF NOT EXISTS CORE.DATA_OWNER
    COMMENT = 'Team or individual responsible for the data';

-- Apply tags
ALTER TABLE CORE.ADJ_HEADER SET TAG
    CORE.DATA_CLASSIFICATION = 'CONFIDENTIAL',
    CORE.DATA_DOMAIN = 'ADJUSTMENT';

ALTER TABLE CORE.ADJ_LINE_ITEM SET TAG
    CORE.DATA_CLASSIFICATION = 'CONFIDENTIAL',
    CORE.DATA_DOMAIN = 'ADJUSTMENT';

ALTER TABLE FACT.FACT_TABLE SET TAG
    CORE.DATA_CLASSIFICATION = 'CONFIDENTIAL',
    CORE.DATA_DOMAIN = 'FACT';

ALTER TABLE CORE.ADJ_STATUS_HISTORY SET TAG
    CORE.DATA_CLASSIFICATION = 'INTERNAL',
    CORE.DATA_DOMAIN = 'AUDIT';

ALTER DYNAMIC TABLE MART.FACT_ADJUSTED SET TAG
    CORE.DATA_CLASSIFICATION = 'CONFIDENTIAL',
    CORE.DATA_DOMAIN = 'FACT';


-- ═════════════════════════════════════════════════════════════════════════════
-- 4. COLUMN-LEVEL MASKING POLICY (optional: mask CREATED_BY for viewers)
-- ═════════════════════════════════════════════════════════════════════════════

CREATE OR REPLACE MASKING POLICY CORE.MASK_USERNAME AS (val VARCHAR)
RETURNS VARCHAR ->
    CASE
        WHEN IS_ROLE_IN_SESSION('ADJ_APPROVER') OR IS_ROLE_IN_SESSION('ADJ_ADMIN')
        THEN val
        ELSE '***MASKED***'
    END;

